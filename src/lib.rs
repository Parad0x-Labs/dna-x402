/// Seed used to derive Nullifier PDAs.
/// Prevents double-spending by tracking consumed notes.
const NULLIFIER_SEED: &[u8] = b"pdx_nullifier";

/// Seed for the Protocol Vault PDA.
/// This account MUST be pre-funded to pay relayers.
const VAULT_SEED: &[u8] = b"pdx_vault";

/// Dynamic fee policy used for relayer payouts.
/// base_fee_lamports: fixed minimum fee component
/// fee_bps: variable component in basis points of withdrawal amount
/// min_settle_lamports: values below this should be netted/streamed off-chain (not rejected on-chain)
#[derive(BorshSerialize, BorshDeserialize, Debug, Clone, Copy)]
pub struct FeePolicy {
    pub base_fee_lamports: u64,
    pub fee_bps: u16,
    pub min_settle_lamports: u64,
}

/// Default policy tuned for micropayment compatibility.
const DEFAULT_FEE_POLICY: FeePolicy = FeePolicy {
    base_fee_lamports: 5_000,  // 0.000005 SOL
    fee_bps: 50,               // 0.50%
    min_settle_lamports: 0,    // never hard-reject tiny withdrawals
};

const NR_PUBLIC_INPUTS: usize = VERIFYINGKEY.nr_pubinputs - 1;

