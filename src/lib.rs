fn validate_public_inputs(
    root: &[u8; 32],
    nullifier_asset: &[u8; 32],
    nullifier_fee: &[u8; 32],
    new_commitment: &[u8; 32],
    asset_id_hash: &[u8; 32],
) -> ProgramResult {
    let inputs = [root, nullifier_asset, nullifier_fee, new_commitment, asset_id_hash];
    for (i, input) in inputs.iter().enumerate() {
        // Reject all-zero inputs (common attack vector)
        if input.iter().all(|&x| x == 0) {
            msg!("PDX_ERROR: Zero input detected at index {}", i);
            return Err(ProgramError::InvalidArgument);
        }
    }

    // Asset and Fee nullifiers must be distinct
    if nullifier_asset == nullifier_fee {
        msg!("PDX_ERROR: Identical Nullifiers.");
        return Err(ProgramError::InvalidArgument);
    }

    Ok(())
}

fn verify_nullifier_unused(
    program_id: &Pubkey,
    pda_account: &AccountInfo,
    nullifier: &[u8; 32],
) -> ProgramResult {
    let (expected_pda, _bump) = Pubkey::find_program_address(&[NULLIFIER_SEED, nullifier], program_id);

    if expected_pda != *pda_account.key {
        msg!("PDX_ERROR: Invalid Nullifier PDA address.");
        return Err(ProgramError::InvalidArgument);
    }

    // If account has data, it means it was already initialized (Spent)
    if !pda_account.data_is_empty() {
        msg!("PDX_ERROR: Double Spend Detected! Nullifier already used.");
        return Err(ProgramError::AccountAlreadyInitialized);
    }
    Ok(())
}

fn consume_nullifier<'a>(
    prog_id: &Pubkey,
    payer: &AccountInfo<'a>,
    pda: &AccountInfo<'a>,
    sys: &AccountInfo<'a>,
    null: &[u8; 32]
) -> ProgramResult {
    let (key, bump) = Pubkey::find_program_address(&[NULLIFIER_SEED, null], prog_id);

    if key != *pda.key {
        return Err(ProgramError::InvalidArgument);
    }
    // Create the account with exactly 1 byte of space.
    // This prevents "Fake Account" attacks where data is empty but account exists.
    invoke_signed(
        &system_instruction::create_account(
            payer.key,
            pda.key,
            Rent::default().minimum_balance(1), // Rent for 1 byte
            1,                                  // Space: 1 byte
            prog_id
        ),
        &[payer.clone(), pda.clone(), sys.clone()],
        &[&[NULLIFIER_SEED, null, &[bump]]]
    )?;

    // Explicitly write "1" to mark as consumed.
    let mut data = pda.try_borrow_mut_data()?;
    data[0] = 1;
    Ok(())
}

fn change_endianness(bytes: &[u8]) -> Vec<u8> {
    let mut vec = Vec::with_capacity(bytes.len());
    for chunk in bytes.chunks(32) {
        for byte in chunk.iter().rev() {
            vec.push(*byte);
        }
    }
    vec
}

fn verify_proof(
    proof_bytes: &[u8],
    public_inputs: &[[u8; 32]],
) -> Result<bool, ProgramError> {
    if proof_bytes.len() != 256 {
        msg!("PDX_ERROR: Invalid Proof Length.");
        return Err(ProgramError::InvalidArgument);
    }
    if public_inputs.len() > NR_PUBLIC_INPUTS {
        msg!(
            "PDX_ERROR: Too many public inputs. Got {}, max {}.",
            public_inputs.len(),
            NR_PUBLIC_INPUTS
        );
        return Err(ProgramError::InvalidArgument);
    }

    let mut prepared_public_inputs = [[0u8; 32]; NR_PUBLIC_INPUTS];
    prepared_public_inputs[..public_inputs.len()].copy_from_slice(public_inputs);

    let proof_a: G1 = G1::deserialize_with_mode(
        &*[&change_endianness(&proof_bytes[0..64]), &[0u8][..]].concat(),
        Compress::No,
        Validate::Yes,
    )
    .map_err(|_| {
        msg!("PDX_ERROR: Invalid Proof A encoding.");
        ProgramError::InvalidArgument
    })?;

    let mut proof_a_neg = [0u8; 65];
    proof_a
        .neg()
        .x
        .serialize_with_mode(&mut proof_a_neg[..32], Compress::No)
        .map_err(|_| {
            msg!("PDX_ERROR: Failed to serialize negated proof A.x.");
            ProgramError::InvalidArgument
        })?;
    proof_a
        .neg()
        .y
        .serialize_with_mode(&mut proof_a_neg[32..], Compress::No)
        .map_err(|_| {
            msg!("PDX_ERROR: Failed to serialize negated proof A.y.");
            ProgramError::InvalidArgument
        })?;

    let proof_a: [u8; 64] = change_endianness(&proof_a_neg[..64])
        .try_into()
        .map_err(|_| {
            msg!("PDX_ERROR: Invalid negated proof A length.");
            ProgramError::InvalidArgument
        })?;
    let proof_b: [u8; 128] = proof_bytes[64..192].try_into().map_err(|_| {
        msg!("PDX_ERROR: Invalid Proof B length.");
        ProgramError::InvalidArgument
    })?;
    let proof_c: [u8; 64] = proof_bytes[192..256].try_into().map_err(|_| {
        msg!("PDX_ERROR: Invalid Proof C length.");
        ProgramError::InvalidArgument
    })?;

    let mut verifier = Groth16Verifier::<NR_PUBLIC_INPUTS>::new(
        &proof_a,
        &proof_b,
        &proof_c,
        &prepared_public_inputs,
        &VERIFYINGKEY,
    )
    .map_err(|err| {
        msg!("PDX_ERROR: Verifier init failed: {:?}", err);
        ProgramError::InvalidArgument
    })?;

    match verifier.verify() {
        Ok(result) => Ok(result),
        Err(err) => {
            msg!("PDX_ERROR: Groth16 verification failed: {:?}", err);
            Ok(false)
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn fee_policy_uses_base_plus_bps() {
        let policy = FeePolicy {
            base_fee_lamports: 5_000,
            fee_bps: 50,
            min_settle_lamports: 0,
        };

        let fee = calculate_relayer_fee(1_000_000, policy).unwrap();
        // 5_000 base + 0.5% of 1_000_000 = 10_000
        assert_eq!(fee, 10_000);
    }

    #[test]
    fn default_policy_does_not_force_min_settle_rejection() {
        assert_eq!(DEFAULT_FEE_POLICY.min_settle_lamports, 0);
        let fee = calculate_relayer_fee(10, DEFAULT_FEE_POLICY).unwrap();
        assert!(fee >= DEFAULT_FEE_POLICY.base_fee_lamports);
    }
}

fn calculate_relayer_fee(amount: u64, policy: FeePolicy) -> Result<u64, ProgramError> {
    let bps_fee = amount
        .checked_mul(policy.fee_bps as u64)
        .ok_or(ProgramError::InvalidArgument)?
        / 10_000;

    policy
        .base_fee_lamports
        .checked_add(bps_fee)
        .ok_or(ProgramError::InvalidArgument)
}
