#[derive(BorshSerialize, BorshDeserialize, Debug)]
pub enum DarkInstruction {
    /// Standard Deposit (Public)
    /// Users deposit SOL/Tokens to mint a private note.
    Deposit {
        amount: u64,
        commitment: [u8; 32]
    },

    /// Private Withdrawal (The "Null" Transaction)
    /// ZK-Verified withdrawal from privacy pool to recipient.
    Withdraw {
        proof: Vec<u8>,             // Groth16 Proof (A[64], B[128], C[64])
        root: [u8; 32],             // Merkle Root
        nullifier_asset: [u8; 32],  // Unique ID for Asset Note
        nullifier_fee: [u8; 32],    // Unique ID for Fee Note
        new_commitment: [u8; 32],   // New Note Output (optional)
        asset_id_hash: [u8; 32],    // INTEGRITY LOCK: keccak256(recipient + amount)
        recipient: Pubkey,          // The recipient who will receive the SOL
        amount: u64,                // Amount to withdraw from pool
    },
}

// -----------------------------------------------------------------------------
// 🚀 ENTRYPOINT
// -----------------------------------------------------------------------------

entrypoint!(process_instruction);

pub fn process_instruction(
    program_id: &Pubkey,
    accounts: &[AccountInfo],
    ix_data: &[u8],
) -> ProgramResult {
    // Attempt to parse instruction
    let ix = DarkInstruction::try_from_slice(ix_data)
        .map_err(|_| ProgramError::InvalidInstructionData)?;

    match ix {
        DarkInstruction::Deposit { amount, commitment } => {
            msg!("PDX_INFO: Deposit Request: {} lamports", amount);
            msg!("PDX_INFO: Commitment: {:?}", commitment);

            // In a real implementation, you would transfer funds from User -> Vault here.
            Ok(())
        }

        DarkInstruction::Withdraw {
            proof,
            root,
            nullifier_asset,
            nullifier_fee,
            new_commitment,
            asset_id_hash,
            recipient,
            amount,
        } => process_withdraw(
            program_id,
            accounts,
            proof,
            root,
            nullifier_asset,
            nullifier_fee,
            new_commitment,
            asset_id_hash,
            recipient,
            amount,
        ),
    }
}

// -----------------------------------------------------------------------------
// 🛡️ CORE LOGIC: PROCESS DEPOSIT
// -----------------------------------------------------------------------------

fn process_deposit(
    program_id: &Pubkey,
    accounts: &[AccountInfo],
    amount: u64,
    commitment: [u8; 32],
) -> ProgramResult {
    let acc_iter = &mut accounts.iter();

    // --- 1. ACCOUNT DECODING ---
    let depositor = next_account_info(acc_iter)?;     // [0] Depositor (Signer, pays amount)
    let vault_pda = next_account_info(acc_iter)?;     // [1] Vault PDA (Receives deposit)
    let system = next_account_info(acc_iter)?;        // [2] System Program

    // --- 2. VALIDATION ---
    let (vault_addr, vault_bump) = Pubkey::find_program_address(&[VAULT_SEED], program_id);
    if *vault_pda.key != vault_addr {
        msg!("PDX_ERROR: Invalid Vault PDA provided.");
        return Err(ProgramError::InvalidArgument);
    }

    // Reject only zero-value deposits. Micropayment batching happens off-chain.
    if amount == 0 {
        msg!("PDX_ERROR: Deposit amount must be > 0 lamports");
        return Err(ProgramError::InvalidArgument);
    }

    // --- 3. EXECUTE DEPOSIT ---
    msg!("PDX_INFO: Processing deposit of {} lamports", amount);

    // Transfer from depositor to vault
    invoke(
        &system_instruction::transfer(
            depositor.key,
            vault_pda.key,
            amount,
        ),
        &[depositor.clone(), vault_pda.clone(), system.clone()],
    )?;

    msg!("PDX_SUCCESS: Deposit completed. Commitment: {:?}", commitment);
    Ok(())
}

// -----------------------------------------------------------------------------
// 🛡️ CORE LOGIC: PROCESS WITHDRAWAL
// -----------------------------------------------------------------------------

fn process_withdraw(
    program_id: &Pubkey,
    accounts: &[AccountInfo],
    proof: Vec<u8>,
    root: [u8; 32],
    nullifier_asset: [u8; 32],
    nullifier_fee: [u8; 32],
    new_commitment: [u8; 32],
    asset_id_hash: [u8; 32],
    recipient: Pubkey,
    amount: u64,
) -> ProgramResult {
    let acc_iter = &mut accounts.iter();

    // --- 1. ACCOUNT DECODING ---

    let payer = next_account_info(acc_iter)?;          // [0] Relayer (Signer, receives fee)
    let recipient = next_account_info(acc_iter)?;      // [1] Recipient (Writable, receives SOL)
    let null_asset_pda = next_account_info(acc_iter)?; // [2] Asset Nullifier PDA (Writable)
    let null_fee_pda = next_account_info(acc_iter)?;   // [3] Fee Nullifier PDA (Writable)
    let vault_pda = next_account_info(acc_iter)?;      // [4] Vault PDA (Source of withdrawal + fee)
    let system = next_account_info(acc_iter)?;         // [5] System Program

    // --- 2. HARDENING: VAULT HEALTH CHECK ---

    // Fail FAST if the relayer cannot be paid.
    // This protects the user from burning a proof for no reason.

    let (vault_addr, vault_bump) = Pubkey::find_program_address(&[VAULT_SEED], program_id);
    if *vault_pda.key != vault_addr {
        msg!("PDX_ERROR: Invalid Vault PDA provided.");
        return Err(ProgramError::InvalidArgument);
    }

    let relayer_fee = calculate_relayer_fee(amount, DEFAULT_FEE_POLICY)?;
    let total_needed = amount
        .checked_add(relayer_fee)
        .ok_or(ProgramError::InvalidArgument)?;
    if **vault_pda.lamports.borrow() < total_needed {
        msg!("PDX_ERROR: Insufficient vault balance! Need {} lamports, have {}", total_needed, **vault_pda.lamports.borrow());
        return Err(ProgramError::InsufficientFunds);
    }

    // Check withdrawal amount limits
    if amount == 0 {
        msg!("PDX_ERROR: Withdrawal amount must be > 0 lamports");
        return Err(ProgramError::InvalidArgument);
    }
    if amount > 10_000_000_000 { // 10 SOL maximum per withdrawal
        msg!("PDX_ERROR: Withdrawal too large (maximum 10 SOL)");
        return Err(ProgramError::InvalidArgument);
    }

    if amount < DEFAULT_FEE_POLICY.min_settle_lamports {
        msg!(
            "PDX_INFO: Amount below min_settle_lamports={}, expecting off-chain netting/streaming",
            DEFAULT_FEE_POLICY.min_settle_lamports
        );
    }

    // --- 3. SECURITY: INTEGRITY VERIFICATION ---

    // The ZK proof verifies 'asset_id_hash'. We must ensure recipient + amount matches it.
    let mut integrity_data = recipient.key.to_bytes().to_vec();
    integrity_data.extend_from_slice(&amount.to_le_bytes());
    let calculated_hash = keccak::hash(&integrity_data);
    if calculated_hash.to_bytes() != asset_id_hash {
        msg!("PDX_CRITICAL: Integrity Mismatch!");
        msg!("Expected: {:?}", asset_id_hash);
        msg!("Actual:   {:?}", calculated_hash.to_bytes());
        return Err(ProgramError::InvalidInstructionData);
    }

    // --- 4. SECURITY: INPUT VALIDATION ---

    validate_public_inputs(&root, &nullifier_asset, &nullifier_fee, &new_commitment, &asset_id_hash)?;

    // --- 5. SECURITY: ZK PROOF VERIFICATION (The Shield) ---

    // This MUST happen before any state change.
    let public_inputs = [root, nullifier_asset, nullifier_fee, new_commitment, asset_id_hash];
    if !verify_proof(&proof, &public_inputs)? {
        msg!("PDX_CRITICAL: ZK Proof Verification Failed.");
        return Err(ProgramError::InvalidArgument);
    }

    // --- 6. SECURITY: NULLIFIER UNIQUENESS ---

    // Ensure these notes haven't been spent before.
    verify_nullifier_unused(program_id, null_asset_pda, &nullifier_asset)?;
    verify_nullifier_unused(program_id, null_fee_pda, &nullifier_fee)?;

    // =====================================================================
    // ⚡ POINT OF NO RETURN: STATE MUTATION STARTS HERE
    // =====================================================================

    // --- 7. CONSUME NULLIFIERS (Burn the Notes) ---

    // We create the PDA accounts and mark them as "Consumed".
    consume_nullifier(program_id, payer, null_asset_pda, system, &nullifier_asset)?;
    consume_nullifier(program_id, payer, null_fee_pda, system, &nullifier_fee)?;

    // --- 8. EXECUTE WITHDRAWAL (The Real Transfer) ---

    // Transfer SOL from Vault -> Recipient
    msg!("PDX_INFO: Withdrawing {} lamports to recipient: {:?}", amount, recipient.key);
    invoke_signed(
        &system_instruction::transfer(
            vault_pda.key,
            recipient.key,
            amount,
        ),
        &[vault_pda.clone(), recipient.clone(), system.clone()],
        &[&[VAULT_SEED, &[vault_bump]]],
    )?;

    // --- 9. PAY THE RELAYER (The Incentive) ---

    // Atomic transfer from Vault -> Relayer.
    msg!("PDX_INFO: Paying Relayer: {} lamports", relayer_fee);
    invoke_signed(
        &system_instruction::transfer(
            vault_pda.key,
            payer.key,
            relayer_fee
        ),
        &[vault_pda.clone(), payer.clone(), system.clone()],
        &[&[VAULT_SEED, &[vault_bump]]]
    )?;

    msg!("PDX_SUCCESS: Withdrawal of {} lamports completed to {:?}", amount, recipient.key);
    msg!("PDX_SUCCESS: Privacy preserved - no wallet linkage possible!");
    Ok(())
}

